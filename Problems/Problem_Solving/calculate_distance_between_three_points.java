package Problems.Problem_Solving;
public class calculate_distance_between_three_points {

	public static void main(String[] args) {
		
		int x1=1; int y1=1;
		int x2=2; int y2=4;
		int x3=3; int y3=6;
		
		System.out.println((int)Math.sqrt((Math.pow(x1-y1,2))+(Math.pow(x2-y2,2))+(Math.pow(x3-y3,2))));
	}

}
